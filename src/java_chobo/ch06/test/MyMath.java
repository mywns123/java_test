package java_chobo.ch06.test;

public class MyMath {
	int a;
	int b;
	MyMath(int a, int b){
		this.a=a;
		this.b=b;
	}
	

	public int add() {		
		return a+b;
	}

	public int sub() {
		return  a-b;
	}

	public int mul() {
		return a*b;
	}

	public int div() {
		return a/b;
	}
	
	
	
	
	

}
